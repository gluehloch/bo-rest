package de.betoffice.web.json;

import java.io.Serializable;

/**
 * Holds the user data.
 * 
 * @author Andre Winkler
 */
public class PartyJson extends AbstractIdentifier implements Serializable {

    private static final long serialVersionUID = -1479900122605812841L;

    private String nickname;
    private String name;
    private String surname;
    private String mail;
    private String phone;
    private String password;
    private String title;

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return "PartyJson [nickname=" + nickname + ", name=" + name
                + ", surname=" + surname + ", mail=" + mail + ", phone=" + phone
                + ", password=" + password + ", title=" + title + "]";
    }

}
